// logical operator perform logical operations and return a boolean value either true or false
//Logical Operator AND =>&& ->true if both operand are true else return false 
//Logical Operator OR=> || ->true if either of the operand is true return false else if both are false
//Logical Operator NOT=>! ->true if the operand is false and vice-versa

// logical AND
console.log(true && true); //true
console.log(true && false); //false

// logical OR
console.log(true || false); //true
console.log(false || false);//fasle

// logical NOT
console.log(!true);